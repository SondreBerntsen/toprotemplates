/* Set the width of the sidebar to 250px and the left margin of the page content to 250px */
function openNav() {
  document.getElementById("sideNavbarC").style.width = "290px";
  document.getElementById("darkOverlay").style.width = "100%";
}
/* Set the width of the sidebar to 0 */
function closeNav() {
  document.getElementById("sideNavbarC").style.width = "0";
  document.getElementById("darkOverlay").style.width = "0";

}